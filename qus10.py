num = int(input("Enter a number (1-7): "))

if num == 1:
    print("1 - Monday")
elif num == 2:
    print("2 - Tuesday")
elif num == 3:
    print("3 - Wednesday")
elif num == 4:
    print("4 - Thursday")
elif num == 5:
    print("5 - Friday")
elif num == 6:
    print("6 - Saturday")
elif num == 7:
    print("7 - Sunday")
else:
    print("Invalid input! Please enter a number between 1 and 7.")
